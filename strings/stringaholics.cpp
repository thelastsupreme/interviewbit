int solve(vector<string> &A) {
    long long int result=1;
    int n=A.size();
    vector<int>sizes;
    vector<int>times(n,1);
    for(int i=0;i<n;i++)
    {
        string temp=A[i];
        for(int j=1;;j++)
        {
            int k=j%A[i].size();
            rotate(A[i].begin(),A[i].begin()+k,A[i].end());
            if(temp==A[i])
            {
                times[i]=j;
                //cout<<"index "<<i<<" "<<times[i]<<endl;
                break;
            }
        }
    }
    long long int M=1000000007;
    for(int i=0;i<n;i++)   // Calculating LCM
    {
        for(int j=i+1;j<n;j++)
        {
            times[j]=times[j]/__gcd(times[i],times[j]);
        }
        result=((result%M)*(times[i]%M))%M;
    }
    return (result)%M;
    return result;


}


